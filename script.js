
      class Ball {
        constructor(x, y, dx, dy, w, h=w, color = '#000') {
          this.x = x;
          this.y = y;
          this.w = w;
          this.h = h;
          this.color = color;
          this.dx = dx;
          this.dy = dy;
          this.speed = 5; 
      

          this.element = document.createElement('div');
      
          this.element.style.width = `${this.w}px`;
          this.element.style.height = `${this.h}px`;
          this.element.style.top = `${this.y}px`;
          this.element.style.left = `${this.x}px`;
          this.element.style.position = 'absolute';
          this.element.style.background = this.color;
          this.element.style.borderRadius = '50%';
      
          document.body.appendChild(this.element);
      
          this.move = this.move.bind(this);
          this.updatePosition();
        }
      
        updatePosition() {
          this.element.style.top = `${this.y}px`;
          this.element.style.left = `${this.x}px`;
        }
      
        move(balls) {
          this.x += this.dx * this.speed;
          this.y += this.dy * this.speed;
      
          //Side walls collision 
          if (this.x <= 0 || this.x + this.w >= window.innerWidth) {
            this.dx *= -1;
          }
      
          //Top and bottom walls collision
          if (this.y <= 0 || this.y + this.h >= window.innerHeight) {
            this.dy *= -1;
          }

          // Collision with other balls 
          for (let ball of balls) {
            if (ball !== this && this.isCollidingWith(ball)) {
              this.dx *= -1;
              this.dy *= -1;
            }
          }
      
          this.updatePosition();
          requestAnimationFrame(() => this.move(balls));
        }
      
        isCollidingWith(otherBall) {
          const dx = this.x - otherBall.x;
          const dy = this.y - otherBall.y;
          const distance = Math.sqrt(dx * dx + dy * dy);
          return distance < (this.w / 2 + otherBall.w / 2);
        }
      }
      
      //Number of balls 
      const balls = [];
      for (let i = 0; i < 50; i++) {
      
        const ball = new Ball(Math.random()*window.innerWidth,Math.random()*window.innerHeight, Math.random() * 2 - 1 + 0.1, Math.random() * 2 - 1 + 0.1, Math.random()*30 );
        balls.push(ball);
      }
      
      balls.forEach(ball => ball.move(balls));
      
           




























































































































































































































































































































































































































































































































































































































